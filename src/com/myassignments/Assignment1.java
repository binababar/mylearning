package com.myassignments;

public class Assignment1 {

	public static void main(String[] args) {
		
		// swap the two numbers with temp variables
		
		int temp, no1=25,no2=45;
		
		System.out.println("Numbers before swapping " +" no1 : "+no1 +"\t " +"no2 : " +no2);
		
		temp=no1;
		no1=no2;
		no2=temp;
		
		System.out.println("Numbers after swapping " +" no1 : "+no1 +"\t " +"no2 : " +no2);
		
		
	}

}
